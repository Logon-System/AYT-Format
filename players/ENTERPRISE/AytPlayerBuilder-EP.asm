Ayt_Player_B2_Start	
		pop hl			; Get Pattern Ptr
		add hl,de		; + offset in Pattern
		ld a,(hl)		; read dave data
Ayt_DavePort	equ $+1
		out (#a0),a 		; Send to dave 
Ayt_Player_B2_End
;-----------------------------------------------------------------------------------------------------------------------------------------------
Ayt_Player_B3_Start
Ayt_PtrJPEndSeq	equ $+1
		jp p,Ayt_NotLastSeq	; 10/10/10/10 
Ayt_MusicCnt equ $+1			; -------------- 
		ld a,0			; 00/07/07/00  Loop counter 
		dec a			; 00/04/04/00  
		jr z,Ayt_MusicEnd	; 00/12/07/00  Loop finished ?
Ayt_MusicCntPtr equ $+1			; 	       Player on mute
		ld (0),a		; 00/00/13/00  Music loops > loop counter upd
Ayt_LoopSeq  equ $+1
		ld sp,0			; 00/00/10/00  Ptr on sequence loop
		jr Ayt_Player_NewSeq	; 00/00/12/00     
		;-------------------------------------
