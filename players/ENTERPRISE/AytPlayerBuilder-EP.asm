;; The sound processor in the ENTERPRISE computer is called Dave (named after its designer, Dave Woodfield). 
;; In some respects, it is quite similar to the AY-3-8912 (for example, a standard 3-channel, 12-bit square wave from 30 Hz to 125 kHz). 
;; However, it is capable of applying distortion to these frequencies using polynomial counters of varying resolutions, processing a high-pass filter 
;; based on another channel, and handling a ring modulator (a form of signal fusion). 
;; Unlike the AY-3-891x, the noise channel is completely independent of the other channels and has its own volume control. 
;; It can drive two speakers per channel and for the noise channel, resulting in a total of eight amplitudes with a 6-bit resolution (compared to four for the AY). 
;; Finally, it is possible to switch the square wave signal from channel 0 to a 6-bit D/A output. 
;; These features, combined with extremely simple and fast access (compared to what exists on the AY for other platforms, and especially the CPC), 
;; make this processor a marvel considering its age.
;;
;; Despite these features, two points are problematic when it comes to transferring data from an AY-3-8912 (or compatible). 
;; The white noise channel is limited to a sound with an "autonomous" frequency of 31.25 kHz or a sound whose frequency is aligned with that of one 
;; of the three tone channels (but on which the size of the polynomial counters, etc., can still be adjusted). 
;; The consequence of this independence is the absence of a mixing register (like R7 on the AY, which merges the noise with one or more channels). 
;; Muting a channel therefore requires setting its volume to 0. 
;; But the most annoying point, in order to "mimic" the AY, is the absence of management of a predefined hardware envelope (attack + cyclic values) whose 
;; periodicity can be parameterized.
;;
;; I won't dwell on the unfortunate history of the Enterprise. 
;; This history led to frequent adaptations of AY music, without any real consideration for its actual capabilities. 
;; It's the same process as the ports of ZX games to the CPC or Enterprise, which didn't truly take advantage of the target machines' capabilities. 
;; Simulating the noise channel can prove problematic if you want to closely replicate the AY, because the latter allows you to define 32 different frequencies, 
;; whereas beyond polynomial counters, Dave only has one frequency, or must "match" it to that of a channel... Solutions exist, but none are ideal. 
;; Furthermore, many AY music tracks utilize hardware amplitude envelope management, which can provide, for example, a form of "smoothing" between notes.
;;
;; Software simulation of these envelopes can be very CPU-intensive. 
;; Several solutions exist, depending on the desired precision. 
;; It's possible to use the Dave interrupt at 1 kHz (20 interrupts per frame), but this requires software smoothing (via a slope adder) to handle the amplitude 
;; updates for the channel(s) involved. It's also possible to use Nick (the sound processor) to generate interrupts every two lines, but this comes at the cost 
;; of a fairly large Line Parameter Table (LPT).
;;
;; Consequently, I converted and adapted the AYT data (without altering its structure) to allow for the rapid processing of YM data intended for AY-3-891x 
;; and converted to AYT without envelope management. One might think the impact would be significant on music that relies almost constantly on these hardware 
;; envelopes, but the difference isn't bad in itself (though I'm not a musician). Based on this, we can consider processing "purely" Dave music using unused 
;; functions via certain trackers that handle them (like Furnace). 
;; This also guarantees a very short execution time in fixed time (provided, of course, that the player doesn't run in video RAM).
;;
;; I've also created a version that handles hardware envelopes via the 1kHz interrupt, but I haven't "finalized" it since I haven't created the builder for 
;; the player and haven't yet automated certain variables (number of bytes per pattern, for example). 
;; The CPU usage of these interrupts, which I've tried to minimize as much as possible, isn't fixed time, since these interrupts are asynchronous with Nick's Vsync.
;; If there is demand, I will finalize this work.:-)
;;
